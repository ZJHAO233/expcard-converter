/**
 * ExpCard Converter - 配置文件
 *
 * 本文件用于自定义试验卡转换工具的识别规则和输出格式。
 * 修改后重启服务即可生效。
 *
 * 配置项说明：
 *   - LOGIC_OPERATORS:      逻辑运算符映射（Excel中的值 → 输出值）
 *   - SPECIAL_SEPARATORS:   特殊分隔符映射（前缀匹配，自动转换）
 *   - ROW_PATTERNS:         行识别规则（支持精确匹配、正则匹配、前缀匹配）
 *   - OUTPUT_FORMAT:        输出格式配置（序号、列表类型、模板等）
 */

const EXPCARD_CONFIG = {

  // ========================================================================
  // 逻辑运算符映射
  // ========================================================================
  // 用途：将Excel中的逻辑运算符转换为输出格式
  // 格式：{ 键: 值 }，键为Excel中的值，值为输出时使用的值
  //
  // 示例：
  //   Excel中写 "与"，输出时显示 "且"
  //   Excel中写 "或取反"，输出时保持 "或取反"
  //
  // 注意：此处定义的键会自动成为逻辑分隔符识别列表
  // ========================================================================
  LOGIC_OPERATORS: {
    与: "且",
    且: "且",
    或: "或",
    或取反: "或取反",
  },

  // ========================================================================
  // 特殊分隔符映射
  // ========================================================================
  // 用途：处理无法正常拼接的特殊逻辑（如"或延时720s"）
  // 格式：{ 键: 值 }，键为特殊值前缀，值为转换后的输出
  //
  // 匹配方式：前缀匹配
  //   - "或延时720s" 匹配 "或延时"，输出 "或"
  //   - "与延时300s" 匹配 "与延时"，输出 "且"
  //
  // 特殊分隔符的位置会被记录，用于后续标记警告
  // ========================================================================
  SPECIAL_SEPARATORS: {
    或延时: "或",
    与延时: "且",
  },

  // ========================================================================
  // 行识别规则
  // ========================================================================
  // 用途：定义各类行的识别条件
  // 格式：{ 规则名称: { type: 匹配类型, ... } }
  //
  // 支持的匹配类型：
  //   - exact:       精确匹配，值在 values 数组中
  //   - regex:       正则匹配，pattern 为正则表达式字符串
  //   - startsWith:  前缀匹配，值在 values 数组中
  //
  // 规则名称说明：
  //   - header:           表头行（匹配后跳过该行）
  //   - chineseTitle:     中文数字标题（输出为 ## 标题）
  //   - subTitle:         子标题（输出为 ### 标题）
  //   - pureNumber:       纯数字行（一级内容）
  //   - subItem:          子项行（二级内容）
  //   - paragraphTitle:   段落标题（跳过内容区域）
  //   - contentStart:     内容起始标记
  //   - skipSectionStart: 跳过区间的开始标记
  //
  // 注意：
  //   1. 正则表达式中的反斜杠需要转义（如 \\d 而非 \d）
  //   2. 不配置的规则会使用内置默认值
  // ========================================================================
  ROW_PATTERNS: {

    // 表头行识别
    // 说明：A列精确匹配 values 中的值时，跳过该行
    // 示例：A列 = "序号" 或 "条件确认" → 跳过
    header: {
      type: "exact",
      values: ["序号", "条件确认"],
      description: "表头行，匹配后跳过该行"
    },

    // 中文数字标题
    // 说明：A列匹配正则时，输出为 ## 标题
    // 示例：A列 = "一、试验内容" → 输出 "## 一、试验内容"
    chineseTitle: {
      type: "regex",
      pattern: "^[一二三四五六七八九十]+、",
      description: "中文数字标题，如 '一、试验内容'"
    },

    // 子标题
    // 说明：A列匹配正则时，输出为 ### 标题
    // 示例：A列 = "1.1 试验条件（与）" → 输出 "### 1.1 试验条件（与）"
    subTitle: {
      type: "regex",
      pattern: "^\\d+\\.[\\u4e00-\\u9fa5]",
      description: "子标题，如 '1.1 试验条件（与）'"
    },

    // 纯数字行（一级内容）
    // 说明：A列匹配正则时，作为一级内容处理
    // 示例：A列 = "1"、"2" → 一级内容
    pureNumber: {
      type: "regex",
      pattern: "^\\d+$",
      description: "纯数字序号，如 '1'、'2'"
    },

    // 子项行（二级内容）
    // 说明：A列匹配正则时，作为二级内容处理
    // 示例：A列 = "1.1"、"1.2" → 二级内容
    subItem: {
      type: "regex",
      pattern: "^\\d+\\.\\d+$",
      description: "子项序号，如 '1.1'、'1.2'"
    },

    // 段落标题
    // 说明：A列精确匹配 values 中的值时，跳过该行及后续内容
    // 示例：A列 = "试验条件" → 设为跳过区间开始
    paragraphTitle: {
      type: "exact",
      values: ["试验条件", "试验恢复", "结论", "存在问题"],
      description: "段落标题，这些标题下的内容不处理"
    },

    // 内容起始标记
    // 说明：A列精确匹配 values 中的值时，开始处理后续内容行
    // 示例：A列 = "试验内容" → 开始处理内容
    contentStart: {
      type: "exact",
      values: ["试验内容"],
      description: "内容区域起始标记"
    },

    // 跳过区间的开始标记
    // 说明：A列精确匹配 values 中的值时，开始跳过内容
    // 示例：A列 = "试验条件" → 开始跳过，直到遇到 contentStart
    skipSectionStart: {
      type: "exact",
      values: ["试验条件"],
      description: "跳过区间开始标记"
    }
  },

  // ========================================================================
  // 输出格式配置
  // ========================================================================
  // 用途：自定义转换后的输出格式
  //
  // 序号设置：
  //   - enabled:   是否启用自动序号
  //   - separator: 层级分隔符（如 "." 或 "-"）
  //   - startNum:  起始序号
  //   - numType:   数字类型（arabic/roman/chinese）
  //
  // 各层级格式：
  //   - type:     列表类型（ordered 有序 / unordered 无序）
  //   - prefix:   Markdown 标题前缀（如 "## "）
  //   - bullet:   无序列表符号（如 "-"、"*"、"+"）
  //   - indent:   缩进级别（数字）
  //   - template: 格式模板
  //     {prefix}  - 标题前缀
  //     {num}     - 序号
  //     {text}    - 文本内容
  //     {indent}  - 缩进
  //     {bullet}  - 无序列表符号
  //
  // 缩进设置：
  //   - size: 每级缩进空格数
  //   - char: 缩进字符（空格或制表符）
  // ========================================================================
  OUTPUT_FORMAT: {

    // 序号设置
    numbering: {
      enabled: true,              // 是否启用自动序号
      separator: ".",             // 层级分隔符
      startNum: 1,                // 起始序号
      numType: "arabic",          // arabic(1,2,3) / roman(I,II,III) / chinese(一,二,三)
    },

    // 各层级格式配置
    levels: {
      // 一级标题（中文数字标题，如"一、试验内容"）
      title: {
        type: "ordered",         // ordered(有序) / unordered(无序)
        prefix: "## ",           // Markdown 标题前缀
        bullet: "-",             // 无序列表符号
        indent: 0,               // 缩进级别
        template: "{prefix}{num} {text}",
      },

      // 二级标题（子标题，如"1.1 试验条件（与）"）
      subTitle: {
        type: "ordered",
        prefix: "### ",
        bullet: "-",
        indent: 0,
        template: "{prefix}{num} {text}",
      },

      // 一级内容（纯数字行）
      content1: {
        type: "ordered",
        prefix: "",
        bullet: "-",
        indent: 0,
        template: "{indent}{num} {text}",
      },

      // 二级内容（子项）
      content2: {
        type: "ordered",
        prefix: "",
        bullet: "-",
        indent: 1,
        template: "{indent}{num} {text}",
      },

      // 三级内容（嵌套分组）
      content3: {
        type: "unordered",
        prefix: "",
        bullet: "-",
        indent: 2,
        template: "{indent}{bullet} {text}",
      },

      // 四级内容（最深层）
      content4: {
        type: "unordered",
        prefix: "",
        bullet: "*",
        indent: 3,
        template: "{indent}{bullet} {text}",
      },
    },

    // 缩进设置
    indent: {
      size: 3,                    // 每级缩进空格数
      char: " ",                  // 缩进字符
    },
  },
};
